CREATE TRIGGER PDONE_BI FOR PDONE ACTIVE
BEFORE insert POSITION 0

AS
DECLARE VARIABLE tmp DECIMAL(18,0);
BEGIN
  IF (NEW.ID IS NULL) THEN
    NEW.ID = GEN_ID(GEN_PDONE_ID, 1);
  ELSE
  BEGIN
    tmp = GEN_ID(GEN_PDONE_ID, 0);
    if (tmp < new.ID) then
      tmp = GEN_ID(GEN_PDONE_ID, new.ID-tmp);
  END
  IF (NEW.DONE_DATE IS NULL) THEN
    NEW.DONE_DATE = CURRENT_TIMESTAMP;
  
END
