ALTER PROCEDURE START_NEW_SESSION (
    PID PCODE )
SQL SECURITY DEFINER

AS
DECLARE variable CHECKED INTEGER;
  DECLARE variable ADDED INTEGER;
  DECLARE variable NOT_FOUND INTEGER;
BEGIN
  SELECT CHECKED,ADDED,NOT_FOUND FROM GET_STATS
    INTO :CHECKED,:ADDED,:NOT_FOUND;
  INSERT INTO PDONE (PID,CHECKED,ADDED,NOT_FOUND) 
    VALUES (:PID,:CHECKED,:ADDED,:NOT_FOUND);
  update qdone set CURRENT_SESSION = False;
  DELETE FROM QDUPLICATES;
  DELETE FROM QERROR;
END
^
