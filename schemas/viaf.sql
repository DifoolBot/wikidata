CREATE TABLE CODES
(
  PID PCODE NOT NULL,
  CODE varchar(25),
  DESCRIPTION varchar(100),
  DO_IGNORE BOOL_FALSE,
  IS_EMPTY BOOL_FALSE,
  CONSTRAINT INTEG_25 PRIMARY KEY (PID)
);
CREATE TABLE PDONE
(
  ID INTCODE NOT NULL,
  PID CODE NOT NULL,
  CHECKED integer,
  ADDED integer,
  NOT_FOUND integer,
  DONE_DATE DATUM,
  CONSTRAINT PK_PDONE_0 PRIMARY KEY (ID)
);
CREATE TABLE QDONE
(
  QCODE QCODE NOT NULL,
  DONE_DATE DATUM,
  CURRENT_SESSION boolean,
  CONSTRAINT INTEG_6 PRIMARY KEY (QCODE)
);
CREATE TABLE QDUPLICATES
(
  ID INTCODE NOT NULL,
  QID QCODE,
  DUPLICATE_QID QCODE,
  LOCAL_AUTH_ID CODE,
  VIAF_ID CODE,
  CONSTRAINT PK_QDUPLICATES_0 PRIMARY KEY (ID),
  CONSTRAINT INTEG_11 UNIQUE (QID,DUPLICATE_QID,LOCAL_AUTH_ID,VIAF_ID)
