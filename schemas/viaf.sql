/* Record that a VIAF id was added to QID (and drop any earlier error for it). */
CREATE PROCEDURE ADD_DONE (QID QID)
SQL SECURITY DEFINER
AS
BEGIN
  DELETE FROM ERRORS WHERE QID = :QID;
  UPDATE OR INSERT INTO ADDED (QID) VALUES (:QID) MATCHING (QID);
END^

/* Record a processing error for QID. */
CREATE PROCEDURE ADD_ERROR (QID QID, MESSAGE MESSAGE)
SQL SECURITY DEFINER
AS
BEGIN
  DELETE FROM ADDED WHERE QID = :QID;
  DELETE FROM ERRORS WHERE QID = :QID;
  INSERT INTO ERRORS (QID, MESSAGE) VALUES (:QID, :MESSAGE);
END^

/* Cache (or refresh) a 'not_found' result for QID under authority source PID. */
CREATE PROCEDURE ADD_NOT_FOUND (QID QID, PID PID)
SQL SECURITY DEFINER
AS
BEGIN
  UPDATE OR INSERT INTO NOT_FOUND (QID, PID, CHECKED_DATE)
    VALUES (:QID, :PID, CURRENT_TIMESTAMP) MATCHING (QID, PID);
END^

/* Record that QID duplicates DUPLICATE_QID within a VIAF cluster. */
CREATE PROCEDURE ADD_DUPLICATE (
  QID QID,
  DUPLICATE_QID QID,
  LOCAL_AUTH_ID AUTH_ID,
  VIAF_ID VIAF_ID )
SQL SECURITY DEFINER
AS
BEGIN
  UPDATE OR INSERT INTO DUPLICATES (QID, DUPLICATE_QID, LOCAL_AUTH_ID, VIAF_ID)
    VALUES (:QID, :DUPLICATE_QID, :LOCAL_AUTH_ID, :VIAF_ID)
    MATCHING (QID, DUPLICATE_QID, LOCAL_AUTH_ID, VIAF_ID);
END^

/* Record a local authority id belonging to QID's VIAF cluster. */
CREATE PROCEDURE ADD_DUPLICATE_LOCAL_AUTH_ID (
  QID QID,
  LOCAL_AUTH_ID AUTH_ID,
  VIAF_ID VIAF_ID )
SQL SECURITY DEFINER
AS
BEGIN
  INSERT INTO DUPLICATE_LOCAL_AUTH_IDS (QID, LOCAL_AUTH_ID, VIAF_ID)
    VALUES (:QID, :LOCAL_AUTH_ID, :VIAF_ID);
END^

/* Mark transient errors so they get retried on the next run. */
CREATE PROCEDURE CLEAN_UP
SQL SECURITY DEFINER
AS
BEGIN
  UPDATE ERRORS SET RETRY = true
    WHERE RETRY = false
      AND (MESSAGE CONTAINING 'connection'
        OR MESSAGE CONTAINING 'object is not iterable'
        OR MESSAGE CONTAINING 'WDQS query failed');
END^

/* Normalize and de-duplicate the duplicate-local-auth-id report. */
CREATE PROCEDURE CLEANUP_DUPLICATE_LOCAL_AUTH_IDS
SQL SECURITY DEFINER
AS
BEGIN
  UPDATE DUPLICATE_LOCAL_AUTH_IDS
    SET LOCAL_AUTH_ID = REPLACE(LOCAL_AUTH_ID, 'http://d-nb.info/gnd/', '')
    WHERE LOCAL_AUTH_ID STARTING WITH 'http://d-nb.info/gnd/';

  DELETE FROM DUPLICATE_LOCAL_AUTH_IDS d1
    WHERE EXISTS (
      SELECT 1 FROM DUPLICATE_LOCAL_AUTH_IDS d2
      WHERE d1.ID > d2.ID
        AND d1.QID = d2.QID
        AND d1.LOCAL_AUTH_ID = d2.LOCAL_AUTH_ID
        AND d1.VIAF_ID = d2.VIAF_ID);
END^

/* Current-session counts for the wiki report. */
CREATE PROCEDURE GET_STATS
RETURNS (CHECKED integer, ADDED integer, NOT_FOUND integer)
SQL SECURITY DEFINER
AS
DECLARE VARIABLE ALL_ERRORS integer;
BEGIN
  ADDED = (SELECT COUNT(*) FROM ADDED);
  ALL_ERRORS = (SELECT COUNT(*) FROM ERRORS);
  NOT_FOUND = (SELECT COUNT(*) FROM ERRORS WHERE MESSAGE CONTAINING 'status not_found');
  CHECKED = ADDED + ALL_ERRORS;
  SUSPEND;
END^

/* Archive this session's stats to PDONE, then empty the per-session tables. */
CREATE PROCEDURE END_SESSION (PID PID)
SQL SECURITY DEFINER
AS
DECLARE VARIABLE V_CHECKED integer;
DECLARE VARIABLE V_ADDED integer;
DECLARE VARIABLE V_NOT_FOUND integer;
BEGIN
  SELECT CHECKED, ADDED, NOT_FOUND FROM GET_STATS
    INTO :V_CHECKED, :V_ADDED, :V_NOT_FOUND;
  INSERT INTO PDONE (PID, CHECKED, ADDED, NOT_FOUND)
    VALUES (:PID, :V_CHECKED, :V_ADDED, :V_NOT_FOUND);
  DELETE FROM ADDED;
  DELETE FROM DUPLICATES;
  DELETE FROM DUPLICATE_LOCAL_AUTH_IDS;
  DELETE FROM ERRORS;
END^

SET TERM ; ^
