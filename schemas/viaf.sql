ALTER PROCEDURE GET_STATS
RETURNS (
    CHECKED integer,
    ADDED integer,
    NOT_FOUND integer )
SQL SECURITY DEFINER

AS
DECLARE variable ALL_ERRORS integer;
BEGIN
  CHECKED = 0;
  ADDED = 0;
  NOT_FOUND = 0;
  select count(*) from qdone where current_session = true into :ADDED;
  select count(*) from qerror into :ALL_ERRORS;
  select count(*) from qerror where error containing 'status not_found' into :NOT_FOUND;

  CHECKED = ADDED + ALL_ERRORS;
  suspend;
END
^
