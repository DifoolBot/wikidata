/* VIAF bot database - Firebird 5 schema. */
/* Modernized from the original: IDENTITY columns and DEFAULT CURRENT_TIMESTAMP */
/* replace the generators + before-insert triggers. */
/* The ADDED / DUPLICATES / DUPLICATE_LOCAL_AUTH_IDS / ERRORS tables only ever */
/* hold the current session (END_SESSION empties them after archiving stats to */
/* PDONE), so no history flag is kept and the database stays small. */
/* CODES and PDONE keep their original names and columns for an external report. */

/******************** DOMAINS *********************/

CREATE DOMAIN QID
 AS varchar(16) CHARACTER SET ASCII COLLATE ASCII;
CREATE DOMAIN PID
 AS varchar(16) CHARACTER SET ASCII COLLATE ASCII;
CREATE DOMAIN VIAF_ID
 AS varchar(25) CHARACTER SET ASCII COLLATE ASCII;
CREATE DOMAIN AUTH_ID
 AS varchar(255) COLLATE UTF8;
CREATE DOMAIN MESSAGE
 AS varchar(255) COLLATE UTF8;

/******************** TABLES **********************/

/* QIDs a VIAF id was added to during the current session. */
CREATE TABLE ADDED
(
  QID QID NOT NULL,
  ADDED_DATE timestamp DEFAULT CURRENT_TIMESTAMP NOT NULL,
  CONSTRAINT PK_ADDED PRIMARY KEY (QID)
);

/* QIDs that could not be processed; RETRY marks transient errors to try again. */
CREATE TABLE ERRORS
(
  QID QID NOT NULL,
  MESSAGE MESSAGE,
  ERROR_DATE timestamp DEFAULT CURRENT_TIMESTAMP NOT NULL,
  RETRY boolean DEFAULT false NOT NULL,
  NOTE MESSAGE,
  CONSTRAINT PK_ERRORS PRIMARY KEY (QID)
);

/* QIDs to skip permanently. */
CREATE TABLE IGNORED
(
  QID QID NOT NULL,
  CONSTRAINT PK_IGNORED PRIMARY KEY (QID)
);

/* (item, authority source) pairs VIAF returned 'not_found' for, cached so they */
/* are not re-queried for a while (the daily VIAF quota is small). The result is */
/* per authority source: an item may fail under one PID but match under another. */
/* Persists across sessions. */
CREATE TABLE NOT_FOUND
(
  QID QID NOT NULL,
  PID PID NOT NULL,
  CHECKED_DATE timestamp DEFAULT CURRENT_TIMESTAMP NOT NULL,
  CONSTRAINT PK_NOT_FOUND PRIMARY KEY (QID, PID)
);

/* Items sharing a VIAF cluster with another item (reported, then cleared). */
CREATE TABLE DUPLICATES
(
  ID integer GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  QID QID,
  DUPLICATE_QID QID,
  LOCAL_AUTH_ID AUTH_ID,
  VIAF_ID VIAF_ID,
  CONSTRAINT PK_DUPLICATES PRIMARY KEY (ID),
  CONSTRAINT UQ_DUPLICATES UNIQUE (QID, DUPLICATE_QID, LOCAL_AUTH_ID, VIAF_ID)
);

/* Items whose VIAF cluster lists several local authority ids (reported, then cleared). */
CREATE TABLE DUPLICATE_LOCAL_AUTH_IDS
(
  ID bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  QID QID,
  LOCAL_AUTH_ID AUTH_ID,
  VIAF_ID VIAF_ID,
  CONSTRAINT PK_DUPLICATE_LOCAL_AUTH_IDS PRIMARY KEY (ID)
);

/* Authority-source config, read by an external report. */
CREATE TABLE CODES
(
  PID PID NOT NULL,
  CODE varchar(25),
  DESCRIPTION varchar(100),
  DO_IGNORE boolean DEFAULT false NOT NULL,
  IS_EMPTY boolean DEFAULT false NOT NULL,
  SORT_ORDER integer,   /* processing order; NULL = default (after prioritised) */
  CONSTRAINT PK_CODES PRIMARY KEY (PID)
);

/* Per-session stats archive, read by an external report. */
CREATE TABLE PDONE
(
  ID integer GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  PID PID,
  CHECKED integer,
  ADDED integer,
  NOT_FOUND integer,
  DONE_DATE timestamp DEFAULT CURRENT_TIMESTAMP NOT NULL,
  CONSTRAINT PK_PDONE PRIMARY KEY (ID)
);

/* Bot state: which source is being processed and how far along it is. One row, */
/* pinned to ID = 1. This lives in the database rather than a JSON file so the */
/* bot and the status webservice read one source of truth, and so the session's */
/* identity cannot drift from the session's data (ADDED / ERRORS / PDONE). */
/* TOTAL_ROWS is the qlever row count when the source's file was fetched; */
/* REMAINING_ROWS counts down as rows are processed, so the two give progress */
/* and, with SESSION_START, an estimate of how long the pass will take. */
CREATE TABLE STATE
(
  ID integer NOT NULL,
  CURRENT_PID PID,
  COOLDOWN_UNTIL date,
  SESSION_START date,
  TOTAL_ROWS integer,
  REMAINING_ROWS integer,
  /* when CODES.DESCRIPTION was last refreshed from the property labels on */
  /* Wikidata; those move rarely, so the bot only re-reads them occasionally */
  DESCRIPTIONS_SYNCED date,
  CONSTRAINT PK_STATE PRIMARY KEY (ID),
  CONSTRAINT CK_STATE_SINGLE_ROW CHECK (ID = 1)
);

/******************** INDEXES *********************/

CREATE INDEX IDX_DUPLICATES_QID ON DUPLICATES (QID);
CREATE INDEX IDX_DUPLICATES_DUPLICATE_QID ON DUPLICATES (DUPLICATE_QID);
CREATE INDEX IDX_DUPLICATE_LOCALS_QID ON DUPLICATE_LOCAL_AUTH_IDS (QID);

/******************* PROCEDURES ******************/

